package lesson3;

public class Literals {
    public static void main(String[] args) {
        boolean b = true;
        char ch = 'a';
        String str = "Java";
        int decimal = 22000;
        long octal = 010L;
        short binary = 0b101;
        int hex = 0x0089807;
        float f = .9f;
        double d = 0.567;
        System.out.println("Boolean literal: " + b);
        System.out.println("Character literal: " + ch);
        System.out.println("String literal: " + str);
        System.out.println("Decimal literal: " + decimal);
        System.out.println("Octal literal: " + octal);
        System.out.println("Binary literal: " + binary);
        System.out.println("Hexadecimal literal: " + hex);
        System.out.println("Float literal: " + f);
        System.out.println("Double literal: " + d);
    }
}
